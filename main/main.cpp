void mrubyTask(void *pvParameters)
{
  mrb_state *mrb = NULL;
#ifndef ENABLE_MIRB
  mrb_value v;
#endif

  M5.begin();

  mrb = mrb_open();
  if (!mrb) {
    M5.Lcd.print("mrb_open() failed.\n");
    goto ERROR;
  }

#ifdef ENABLE_MIRB
  mirb(mrb);
#else
  v = mrb_load_irep(mrb, appbin);
  if (mrb->exc) {
    mrb_print_error(mrb);
    mrb->exc = 0;
  }
  else {
    Serial.printf(" => ");
    mrb_p(mrb, v);
  }
#endif

  mrb_close(mrb);

ERROR:
  while (1) {
    micros();
  }
}

extern "C" void app_main()
{
  initArduino();
  xTaskCreatePinnedToCore(mrubyTask, "mrubyTask", 8192, NULL, 1, NULL, ARDUINO_RUNNING_CORE);
}
